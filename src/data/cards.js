function hashName(name) {
  let hash = 0;
  for (const char of name) hash = (hash * 31 + char.charCodeAt(0)) >>> 0;
  return hash;
}

function statFor(name, category, stat, index, power) {
  const profile = powerProfiles[power];
  const modifier = categoryProfiles[category].modifiers[stat] || 0;
  const noise = (hashName(`${name}:${stat}:${index}:${power}`) % profile.spread) - Math.floor(profile.spread / 2);
  return Math.max(25, Math.min(99, profile.base + modifier + noise));
}

function makeCard([name, category, power], index) {
  const profile = powerProfiles[power];
  const attack = statFor(name, category, 'attack', index, power);
  const defense = statFor(name, category, 'defense', index, power);
  const influence = statFor(name, category, 'influence', index, power);
  const strategy = statFor(name, category, 'strategy', index, power);
  const charisma = statFor(name, category, 'charisma', index, power);
  const rating = attack + defense + influence + strategy + charisma;
  const ratingBonus = Math.max(0, rating - 230) * 18;
  const powerBonus = power * 350;

  return {
    name,
    image_url: null,
    rarity: profile.rarity,
    category,
    description: `${categoryProfiles[category].description} Power ${power}/5 med totalrating ${rating}.`,
    attack,
    defense,
    influence,
    strategy,
    charisma,
    sell_value: Math.round((profile.sell + ratingBonus + powerBonus) / 50) * 50
  };
}

export const seedCards = roster
  .map((entry, index) => makeCard(entry, index))
  .sort((a, b) => {
    const ratingA = a.attack + a.defense + a.influence + a.strategy + a.charisma;
    const ratingB = b.attack + b.defense + b.influence + b.strategy + b.charisma;
    return ratingB - ratingA || b.influence - a.influence || b.sell_value - a.sell_value || a.name.localeCompare(b.name);
  });
